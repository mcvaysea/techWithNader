console.log("<------Boolean Operators------>");

console.log("<----Strict Equality---->");

console.log(5 === 5);
console.log(5 === 4);
console.log(4 === 5);


console.log("<----Strict Not Equality---->");

console.log(5 !== 5);
console.log(4 !== 5);
console.log(5 !== 4);

console.log("<----GreaterThan / LessThan---->");

console.log(9 > 7);
console.log(6 < 5);
console.log(3 < 3);
console.log(3 <= 3);
console.log(4 >= 2);


console.log("<----Logical AND/OR---->");
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
