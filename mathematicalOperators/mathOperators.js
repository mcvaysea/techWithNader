/////////////////////////
//Simple math operators
////////////////////////
// ADDITION
let age = 10 + 23;
console.log(age);

//SUBTRACTION
let points = 150 - 12;
console.log(points);

//MULTIPLICATION
let score = 7 * 23;
console.log(score);

//DIVISION
let divide1 = 100 / 10;
console.log(divide1);

let divide2 = 100 / 3;
console.log(divide2);

//Modulus (remainder)
let mod1 = 6 % 3;
console.log(mod1);

let mod2 = 6 % 2;
console.log(mod2);

let mod3 = 7 % 3;
console.log(mod3);

let mod4 = 7 % 6;
console.log(mod4);

//EXPONENTS
let exp1 = 4 ** 2;
console.log(exp1);

let exp2 = 4 ** 3;
console.log(exp2);


